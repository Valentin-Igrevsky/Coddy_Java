
public class Main {
    public static void main(String[] args) {
        System.out.println("Валентин"); // Вывод строки "Валентин" в консоль
        System.out.println(456); // Вывод числа 456 в консоль

        // Переменные и примитивные типы данных
        int abc = 456; // Целые числа
        float abc_float = 43.54f; // Дробные числа
        double abc_double = 43.54; // Большие* дробные числа
        boolean abc_boolean_t = true; // Небо синее - правда
        boolean abc_boolean_f = false; // Трава красная - ложь
        char abc_char = 432; // Один символ
        String abc_string = "Valentin"; // Текст (String с английского "Строка"; Не примитивный тип, но очень важный)

        // Математические операции
        System.out.println(2 + 2); // Сложение
        System.out.println(100 - 50); // Вычетание
        System.out.println(12 * 16); // Умножение
        System.out.println(4 / 2); // Деление

        // Вывеод значения переменной в консоль
        System.out.println(abc);

    }
}